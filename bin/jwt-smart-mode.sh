# Functions for encoding/decoding
decode_base64url() {
    local input="$1"
    local padding=$((4 - ${#input} % 4))
    if [ $padding -ne 4 ]; then
        input="${input}$(printf '=%.0s' $(seq 1 $padding))"
    fi
    echo "$input" | base64 -d 2>/dev/null
}

encode_base64url() {
    echo -n "$1" | base64 -w0 | sed 's/+/-/g; s|/|_|g; s/=//g'
}

